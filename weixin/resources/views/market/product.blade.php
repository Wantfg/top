<script>
    (function() {
        $('.js-hover').hover();
        var $paginationSwitch = $('.swiper-pagination .swiper-pagination-switch'),
            ACTIVE_CLASS_NAME = 'swiper-active-switch';
        var goodsSwiper = $('.swiper-container').swiper({
            mode: 'horizontal',
            onSlideChangeStart: function(swiper) {
                $paginationSwitch.removeClass(ACTIVE_CLASS_NAME);
                $paginationSwitch.eq(swiper.activeIndex).addClass(ACTIVE_CLASS_NAME);
            }
        });
        $paginationSwitch.on('click mouseover', function() {
            var $this = $(this),
                index = $this.data('index');
            goodsSwiper.swipeTo(index, 750, true);
        });
        var $paginationList = $('.swiper-pagination-list');
        $('.swiper-prev').on('click', function() {
            goodsSwiper.swipePrev();
            var $element = $paginationSwitch.eq(goodsSwiper.activeIndex);
            var left = $element.position().left,
                scrollLeft = $paginationList.scrollLeft();
            if (left >= $paginationList.width() || left < scrollLeft) {
                $paginationList.animate({
                    scrollLeft: left
                }, 100);
            }
        });
        $('.swiper-next').on('click', function() {
            goodsSwiper.swipeNext();
            var $element = $paginationSwitch.eq(goodsSwiper.activeIndex);
            var left = $element.position().left;
            if (left >= $paginationList.width()) {
                $paginationList.animate({
                    scrollLeft: left
                }, 100);
            }
        });
    })();
</script>
